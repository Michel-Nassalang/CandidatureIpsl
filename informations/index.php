                    <h2 class="mb-4">Présentation de l'Institut</h2>
                    <p>
                        L'Institut Polytechnique de Saint-Louis avec comme acronyme IPSL est une structure spécialisé dans 
                        la formation d'ingénieurs. Il a été mis en place en 2012 par l'Etat du Sénégal. Il est basé à Saint-Louis 
                        au niveau de l'université Gaston Berger. <br>
                        L'institut a pour vocation de former de bons ingénieurs dans différnts domaines pour fournir à la demande 
                        des professionnels de tailles pour certains travaux dans la sous région. La formation impose une grande discipline 
                        avec une rigueur impressionnante et régulière. <br>
                        La formation reste axée sur deux parties: les classes préparatoires, le cycle ingénieur. 
                        <ul>
                            <li>
                                <p>
                                    Les classes préparatoires s'etalent sur deux années consécutives et elles prennent en charge la base solide que 
                                    doit avoir l'ingénieur. Dans cette partie, l'élève ingénieur doit emmagasiner le maximum de connaissances pouvant 
                                    l'attribuer le titre de chef de projet, ou leader dans un groupe car il devra pouvoir gérer un maximum de situations 
                                    dans différent domaine. L'élève ingénieur en classes préparatoires doit acquérir la polyvalence dans cette étape de 
                                    sa formation.
                                </p>
                            </li>
                            <li>
                                <p>
                                    Le cycle ingénieur est une partie où l'élève ingénieur doit se spécialiser et suivre une filière de carrière bien précise. 
                                    Ce cycle dure trois années de spécialisation dans un domaine tels que la mécanique, l'informatique... 
                                    Il prépare l'élève pour son entrée en milieu professionnel en lui donnant tout le nécessaire pour bien gérer un travail à l'avenir. 
                                    Cela se fera principalement à des activités de pratiques similaires à celles qui se font en entreprise. 
                                </p>
                            </li>
                        </ul>
                    </p>
